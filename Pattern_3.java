package program;

import java.util.Scanner;

public class Pattern_3 {
public static void main(String[] args) {
	Scanner sc=new Scanner(System.in);
	int n =sc.nextInt();
	
	for(int i=n;i>=1;i--)
	{
		
		for(int j=i;j<=n;j++)
		{
			
			System.out.print(j);
			
	
			
		}
		System.out.println();
	}
			
}
}
